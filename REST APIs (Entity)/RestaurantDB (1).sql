/* Part 2 */

CREATE TABLE Locations (
    Locationid     int
        CONSTRAINT PK_LocationID
            PRIMARY KEY               NOT NULL,
    [LocationName] varchar(85)  NOT NULL
);
go

CREATE TABLE Items (
    Itemid    int
        CONSTRAINT PK_ItemID
            PRIMARY KEY               NOT NULL,
    [ItemName] varchar(85)  NOT NULL,
	ItemPrice float not null
);
go

CREATE TABLE Customers (
    Cid     int
        CONSTRAINT PK_CustomerID
            PRIMARY KEY               NOT NULL,
    [FName] varchar(85)  NOT NULL,
	[LName] varchar(85)  NOT NULL,
	[Phone] varchar(14)  NOT NULL,
	[Email] varchar(30)  NULL,
);
go
CREATE TABLE ItemsOffered (
    Locationid     int
	CONSTRAINT FK_ItemsOffered_Locations
        FOREIGN KEY REFERENCES Locations(Locationid)
                                        NOT NULL,
	Itemid		   int
	CONSTRAINT FK_ItemsOffered_Items
        FOREIGN KEY REFERENCES Items(Itemid)
                                        NOT NULL,
	[OfferedStatus]              bit -- 0 or 1 - offered
        CONSTRAINT DF_OfferedStatus  DEFAULT (1)
                                        NOT NULL,
	CONSTRAINT PK_ItemsOffered_LocId_ItemId 
        PRIMARY KEY (Locationid, Itemid)
);
go

CREATE TABLE Orders (
    OrderId     int identity(1,1) NOT NULL,
	Locationid     int
	CONSTRAINT FK_Orders_Locations
        FOREIGN KEY REFERENCES Locations(Locationid)
                                        NOT NULL,
	Cid		   int
	CONSTRAINT FK_Orders_Customers
        FOREIGN KEY REFERENCES Customers(Cid)
                                        NOT NULL,
	PaymentMethod varchar(20)  NULL,
	Itemid		   int
	CONSTRAINT FK_Orders_Items
        FOREIGN KEY REFERENCES Items(Itemid)
                                        NOT NULL,
	ItemCount int NOT NULL,
	CONSTRAINT PK_Orders_Location_Order 
        PRIMARY KEY (Locationid, OrderId),
	OrderDate datetime null
	Constraint DF_OrderDate Default Getdate()
);
go

